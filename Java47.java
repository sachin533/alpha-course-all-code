public class Java47 {
    /*Different Function of calculate [ INT ] */
    public static int sum (int a ,int b){
        return a + b;
    }
    /*Different Function of calculate  [ FLOAT ] */
    public static float sum (float a, float b){
        return a + b;
    }
    public static void main (String[] args){
       System.out.println(sum(7,9));
       System.out.println(sum(7.3f,9.0f));

    }
    
}
