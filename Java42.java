/* swap value */
public class Java42 {
    public static void main(String[] args) {
        // swap value exchange
        int a = 10;
        int b = 5;

        // swap
        int temp = a;
        a = b;
        b = temp;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }

}
