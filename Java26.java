import javax.print.attribute.standard.MediaSize.Engineering;

/*Switch Statement */
public class Java26 {
    public static void main(String[] args) {
        int number = 2;

        switch (number) {

            case 1:
                System.out.println("Police");
                break;

            case 2:
                System.out.println("PM");
                break;

            case 3:
                System.out.println("Enginer");
                break;

            default:
                System.out.println("GYM");
        }
    }

}
