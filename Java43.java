/*Product of Two Number */
public class Java43 {
    public static int multiple(int a, int b) {
        int product = a * b;
        return product;
    }

    public static void main(String[] args) {
        int a = 5;
        int b = 8;

        int prou = multiple(a, b);
        System.out.println("a * b =" + prou);

    }

}
